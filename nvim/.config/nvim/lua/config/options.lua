-- 基本オプション設定

local opt = vim.opt

-- =============================================================================
-- 表示
-- =============================================================================

-- 行番号を表示する
opt.number = true
-- 相対行番号を表示する（カーソル位置からの距離）
opt.relativenumber = true
-- カーソル行をハイライト
opt.cursorline = true
-- シンタックスカラーの最大カラム数
opt.synmaxcol = 240
-- 画面端での折り返しを無効化
opt.wrap = false
-- スクロール時にカーソル上下に確保する行数
opt.scrolloff = 8
-- スクロール時にカーソル左右に確保するカラム数
opt.sidescrolloff = 8
-- 常にサインカラム（leftbar）を表示してガタつきを防ぐ
opt.signcolumn = "yes"
-- 256色対応ターミナルのサポート
opt.termguicolors = true
-- コマンドライン領域の高さ（0 でステータスライン統合時に非表示）
opt.cmdheight = 0
-- ポップアップメニューの最大表示件数
opt.pumheight = 10

-- =============================================================================
-- インデント・タブ
-- =============================================================================

-- タブをスペースに展開する
opt.expandtab = true
-- タブ幅
opt.tabstop = 2
-- シフト幅（>> << 操作時）
opt.shiftwidth = 2
-- 自動インデントを有効化
opt.smartindent = true

-- =============================================================================
-- 検索
-- =============================================================================

-- 検索パターンに大文字が含まれる場合のみ大文字小文字を区別する
opt.ignorecase = true
opt.smartcase = true
-- 検索結果をリアルタイムでハイライト
opt.incsearch = true
-- 検索結果のハイライトを有効化
opt.hlsearch = true

-- =============================================================================
-- ファイル
-- =============================================================================

-- スワップファイルを作成しない
opt.swapfile = false
-- バックアップファイルを作成しない
opt.backup = false
-- アンドゥ履歴をファイルに永続化する
opt.undofile = true
-- アンドゥ履歴の最大件数
opt.undolevels = 10000

-- =============================================================================
-- 編集
-- =============================================================================

-- クリップボードをシステムクリップボードと共有
opt.clipboard = "unnamedplus"
-- バックスペースで何でも削除できるようにする
opt.backspace = "indent,eol,start"
-- 補完メニューの挙動設定
opt.completeopt = "menu,menuone,noselect"
-- 分割ウィンドウを右・下に開く
opt.splitright = true
opt.splitbelow = true
-- updatetime を短縮して CursorHold イベントを速くする（LSP 等に影響）
opt.updatetime = 200
-- キーシーケンスのタイムアウト（ms）
opt.timeoutlen = 300
