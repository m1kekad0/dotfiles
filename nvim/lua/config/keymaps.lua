-- キーマップ設定

-- ローカルエイリアス
local map = vim.keymap.set

-- Leader キーをスペースに設定
vim.g.mapleader = " "
vim.g.maplocalleader = " "

-- =============================================================================
-- 一般
-- =============================================================================

-- ESC でハイライトをクリア
map("n", "<Esc>", "<cmd>nohlsearch<CR>", { desc = "ハイライトをクリア" })

-- ファイルを保存
map("n", "<leader>w", "<cmd>w<CR>", { desc = "ファイルを保存" })

-- Neovim を終了
map("n", "<leader>q", "<cmd>q<CR>", { desc = "終了" })

-- =============================================================================
-- ウィンドウ操作
-- =============================================================================

-- ウィンドウ間の移動（Ctrl + hjkl）
map("n", "<C-h>", "<C-w>h", { desc = "左のウィンドウに移動" })
map("n", "<C-j>", "<C-w>j", { desc = "下のウィンドウに移動" })
map("n", "<C-k>", "<C-w>k", { desc = "上のウィンドウに移動" })
map("n", "<C-l>", "<C-w>l", { desc = "右のウィンドウに移動" })

-- ウィンドウのリサイズ
map("n", "<C-Up>", "<cmd>resize +2<CR>", { desc = "ウィンドウを上に拡大" })
map("n", "<C-Down>", "<cmd>resize -2<CR>", { desc = "ウィンドウを下に縮小" })
map("n", "<C-Left>", "<cmd>vertical resize -2<CR>", { desc = "ウィンドウを左に縮小" })
map("n", "<C-Right>", "<cmd>vertical resize +2<CR>", { desc = "ウィンドウを右に拡大" })

-- =============================================================================
-- バッファ操作
-- =============================================================================

-- 次・前のバッファに移動
map("n", "<S-l>", "<cmd>bnext<CR>", { desc = "次のバッファ" })
map("n", "<S-h>", "<cmd>bprevious<CR>", { desc = "前のバッファ" })

-- 現在のバッファを閉じる
map("n", "<leader>bd", "<cmd>bdelete<CR>", { desc = "バッファを閉じる" })

-- =============================================================================
-- 編集
-- =============================================================================

-- ビジュアルモードで選択範囲を移動（インデントを保持）
map("v", "J", ":m '>+1<CR>gv=gv", { desc = "選択行を下に移動" })
map("v", "K", ":m '<-2<CR>gv=gv", { desc = "選択行を上に移動" })

-- ビジュアルモードでインデント後も選択を維持
map("v", "<", "<gv", { desc = "インデントを減らす" })
map("v", ">", ">gv", { desc = "インデントを増やす" })

-- ペースト時にレジスタを上書きしない
map("v", "p", '"_dP', { desc = "レジスタを保持してペースト" })

-- =============================================================================
-- ターミナル
-- =============================================================================

-- ターミナルモードで ESC でノーマルモードに戻る
map("t", "<Esc><Esc>", "<C-\\><C-n>", { desc = "ターミナルを抜ける" })
